#include <iostream>
#include <queue>
#include <vector>
using namespace std;

int orangesRotting(vector<vector<int>> &grid) {
  int m = (int)grid.size();
  int n = (int)grid[0].size();
  queue<pair<int, int>> q;
  int fresh = 0;

  for (int i = 0; i < m; i++) {
    for (int j = 0; j < n; j++) {
      if (grid[i][j] == 2) {
        q.push({i, j});
      } else if (grid[i][j] == 1) {
        fresh++;
      }
    }
  }

  if (fresh == 0) {
    return 0;
  }

  int minutes = 0;
  int directions[4][2] = {{0, 1}, {1, 0}, {0, -1}, {-1, 0}};

  while (!q.empty()) {
    int size = (int)q.size();
    bool current_min = false;

    for (int i = 0; i < size; i++) {
      auto [x, y] = q.front();
      q.pop();

      for (int d = 0; d < 4; d++) {
        int nx = x + directions[d][0];
        int ny = y + directions[d][1];

        if (nx >= 0 && nx < m && ny >= 0 && ny < n && grid[nx][ny] == 1) {
          grid[nx][ny] = 2;
          q.push({nx, ny});
          fresh--;
          current_min = true;
        }
      }
    }

    if (current_min) {
      minutes++;
    }
  }

  if (fresh == 0) {
    return minutes;
  }
  return -1;
}

int main() {
  vector<vector<int>> grid = {{2, 1, 1}, {1, 1, 0}, {0, 1, 1}};
  cout << orangesRotting(grid) << endl;
  return 0;
}
